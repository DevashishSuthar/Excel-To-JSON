const mongoose = require('mongoose');
const Schema = mongoose.Schema;
let excelSheet = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    country: {
        type: String,
        required: true
    }
})
module.exports = mongoose.model('ExcelSheet', excelSheet);